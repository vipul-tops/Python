"""How will you remove last object from a list? 
Suppose list1 is [2, 33, 222, 14, and 25], what is list1 [-1]? """


list1=[2,33,222,14,25]
list1.pop() #defult remove last object
print(list1)


list1=[2,33,222,14,25]
list1.remove(25) #argument are must given for remove those object
print(list1)


list1=[2,33,222,14,25]

print(list1[-1])
